#include "3-calc.h"

/**
 * add_f - addition of 2 nuts
 * @a: int 1
 * @b: int 2
 *
 * Return: (@a + @b)
 */
int add_f(int a, int b)
{
	return (a + b);
}

/**
 * sub_f - subtraction of 2 numbers
 * @a: int 1
 * @b: int 2
 *
 * Return: (@a - @b)
 */
int sub_f(int a, int b)
{
	return (a - b);
}

/**
 * mul_f - multiplication of 2 numbers
 * @a: int 1
 * @b: int 2
 *
 * Return: (@a * @b)
 */
int mul_f(int a, int b)
{
	return (a * b);
}

/**
 * div_f - division of 2 numbers
 * @a: int 1
 * @b: int 2
 *
 * Return: (@a / @b)
 */
int div_f(int a, int b)
{
	return (a / b);
}

/**
 * mod_f - remainder from division of 2 numbers
 * @a: int 1
 * @b: int 2
 *
 * Return: (@a % @b)
 */
int mod_f(int a, int b)
{
	return (a % b);
}
